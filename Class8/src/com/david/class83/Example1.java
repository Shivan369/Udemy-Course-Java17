package com.david.class83;

public class Example1 {
	public static void main(String[] args) {
		System.out.println("constants in math class");
		System.out.println(" value of pi = "+Math.PI);
		System.out.println(" value of e = "+Math.E);
		System.out.println("Math class methods");
		System.out.println(" absolute value of -33 = "+ Math.abs(-33));
		System.out.println(" Math ceil of 23.43 = "+Math.ceil(23.43));
		System.out.println(" Math floor of 23.43 = "+Math.floor(23.43));
		System.out.println(" value of e2 = "+Math.exp(2));
		System.out.println("increment of 33 is "+Math.incrementExact(33));
		System.out.println(" 10 to power 2 is "+Math.pow(10,2));
		System.out.println(" max value of 44,43 = "+Math.max(44, 43));
	}
}	
