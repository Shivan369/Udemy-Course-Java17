package com.david.class15;

public class Example1 {
	public static void main(String[] args) {
		int v = 234;
		System.out.println("value of int v ="+v);
		double d = 223.33d;
		System.out.println("value of double d ="+d);
		int a = 0b1010;
		System.out.println("value of binary Literal a ="+a);
		int b = 42__4;
		System.out.println("value of underscore literal b ="+b);
		var t =122;
		System.out.println("Type inference t = "+t);
	}
}
